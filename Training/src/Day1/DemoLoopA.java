package Day1;

public class DemoLoopA {

	public static void main(String[] args) {
		
		for(int i=0;i<10;i++) {
			System.out.println(i);
			
		}
		
		System.out.println("================================================");
		for(int i=0;i<10;i++) {
			int count=0;
			System.out.println(count);
			count++;
		}
		
		System.out.println("================================================");
		
		for(int i=0;i<10;i++) {
			int count=0;
			count=i;
			System.out.println(count);
		}
		
		System.out.println("================================================");
		int count=0;
		
		for(int i=0;i<10;i++) {
			System.out.println(count);
			count++;
		}
		
		

	}

}
